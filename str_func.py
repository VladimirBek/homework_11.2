def str_func(string):
    """

    :param string: объект типа str
    :return: строку с большими буквами в каждом слове
    """
    return string.upper()


def str_func_ver2(string):
    """

    :param string: объект типа str
    :return: строку с заглавными буквами в каждом слове
    """
    return string.title()
